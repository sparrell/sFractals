-module('sFractals_app').

-behaviour(application).

%% Application callbacks
-export([start/2
        ,start/0
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    'sFractals_sup':start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

%%--------------------------------------------------------------------
start() ->
    application:ensure_all_started(sFractals).
